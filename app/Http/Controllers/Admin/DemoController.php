     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $data = Demo::findOrFail($id);
        if($data->file != "public/file/demo/file_default.pdf")
            Storage::disk('local')->delete( $data->file );
        $data->delete();

        return redirect()->route('admin.demo.index')->with('success', 'Data Berhasil Dihapus!');
